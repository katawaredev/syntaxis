#!/usr/bin/env node

import { createHash } from "node:crypto";
import { existsSync, readFileSync, renameSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptPath = fileURLToPath(import.meta.url);
const root = resolve(dirname(scriptPath), "..");
const packageRoot = dirname(fileURLToPath(import.meta.resolve("@earendil-works/pi-coding-agent/package.json")));
const manifest = JSON.parse(readFileSync(resolve(packageRoot, "package.json"), "utf8"));
const declarations = readFileSync(resolve(packageRoot, "dist/core/settings-manager.d.ts"), "utf8");
const outputPath = resolve(root, "src/ai/generated_settings.rs");

const settings = [
  ["Defaults", "defaultProvider", "Default provider", "Provider used for new chats", "text", "", "setDefaultProvider"],
  ["Defaults", "defaultModel", "Default model", "Model ID used for new chats", "text", "", "setDefaultModel"],
  ["Defaults", "defaultThinkingLevel", "Thinking level", "Reasoning depth used for new chats", "select:off|minimal|low|medium|high|xhigh|max", "medium", "setDefaultThinkingLevel"],
  ["Conversation", "compaction.enabled", "Auto-compact", "Compact context automatically when it grows too large", "toggle", "true", "setCompactionEnabled"],
  ["Conversation", "retry.enabled", "Automatic retries", "Retry transient model-provider failures", "toggle", "true", "setRetryEnabled"],
  ["Conversation", "steeringMode", "Steering mode", "How steering messages are delivered while Pi is working", "select:one-at-a-time|all", "one-at-a-time", "setSteeringMode"],
  ["Conversation", "followUpMode", "Follow-up mode", "How queued follow-up messages are delivered", "select:one-at-a-time|all", "one-at-a-time", "setFollowUpMode"],
  ["Network", "transport", "Transport", "Preferred transport for providers supporting multiple transports", "select:auto|sse|websocket|websocket-cached", "auto", "setTransport"],
  ["Network", "httpIdleTimeoutMs", "HTTP idle timeout", "Maximum idle gap while waiting for provider traffic, in milliseconds; zero disables it", "number", "300000", "setHttpIdleTimeoutMs"],
  ["Resources", "enableSkillCommands", "Skill commands", "Register installed skills as /skill:name commands", "toggle", "true", "setEnableSkillCommands"],
  ["Images", "images.autoResize", "Auto-resize images", "Resize large images for model compatibility", "toggle", "true", "setImageAutoResize"],
  ["Images", "images.blockImages", "Block images", "Prevent images from being sent to model providers", "toggle", "false", "setBlockImages"],
  ["Privacy", "enableInstallTelemetry", "Install telemetry", "Send Pi's anonymous version/update ping", "toggle", "true", "setEnableInstallTelemetry"],
  ["Startup", "quietStartup", "Quiet startup", "Reduce Pi startup output", "toggle", "false", "setQuietStartup"],
];

for (const [, path, , , , , method] of settings) {
  const topLevel = path.split(".")[0];
  if (!new RegExp(`\\n\\s*${topLevel}\\?:`).test(declarations)) {
    throw new Error(`Pi ${manifest.version} no longer declares setting ${path}`);
  }
  if (!new RegExp(`\\n\\s*${method}\\(`).test(declarations)) {
    throw new Error(`Pi ${manifest.version} no longer exposes ${method} for ${path}`);
  }
}

const cacheKey = createHash("sha256")
  .update("syntaxis-pi-settings-v1\0")
  .update(readFileSync(scriptPath))
  .update(manifest.version)
  .update(declarations)
  .digest("hex");

if (existsSync(outputPath)) {
  const header = readFileSync(outputPath, "utf8").slice(0, 220);
  if (header.includes(`pi-settings-cache-key: ${cacheKey}`)) {
    console.log(`Pi settings metadata is current (${cacheKey.slice(0, 12)})`);
    process.exit(0);
  }
}

const rust = value => JSON.stringify(value);
const rows = settings.map(([section, path, label, description, control, defaultValue, method]) => {
  let kind;
  if (control === "toggle") kind = "PiSettingKind::Toggle";
  else if (control === "number") kind = "PiSettingKind::Number";
  else if (control === "text") kind = "PiSettingKind::Text";
  else kind = `PiSettingKind::Select(&[${control.slice(7).split("|").map(rust).join(", ")}])`;
  return `    PiSettingDefinition { section: ${rust(section)}, path: ${rust(path)}, label: ${rust(label)}, description: ${rust(description)}, kind: ${kind}, default_value: ${rust(defaultValue)}, setter: ${rust(method)} },`;
});

const generated = `// @generated by scripts/generate-pi-settings.mjs; do not edit.\n// pi-settings-cache-key: ${cacheKey}\n// pi-coding-agent: ${manifest.version}\n\n#[derive(Clone, Copy, Debug, PartialEq, Eq)]\npub enum PiSettingKind {\n    Toggle,\n    Select(&'static [&'static str]),\n    Number,\n    Text,\n}\n\n#[derive(Clone, Copy, Debug, PartialEq, Eq)]\npub struct PiSettingDefinition {\n    pub section: &'static str,\n    pub path: &'static str,\n    pub label: &'static str,\n    pub description: &'static str,\n    pub kind: PiSettingKind,\n    pub default_value: &'static str,\n    pub setter: &'static str,\n}\n\n#[cfg_attr(not(feature = "server"), allow(dead_code))]\npub const PI_SETTINGS_SCHEMA_VERSION: &str = ${rust(manifest.version)};\n\npub const PI_SETTING_DEFINITIONS: &[PiSettingDefinition] = &[\n${rows.join("\n")}\n];\n`;

const temporaryPath = `${outputPath}.tmp`;
writeFileSync(temporaryPath, generated);
renameSync(temporaryPath, outputPath);
console.log(`Generated Pi ${manifest.version} settings metadata (${cacheKey.slice(0, 12)})`);
